from math import sqrt

def main(n1, n2):
    print(n1 + n2)
    print(n1 - n2)
    print(n1 * n2)
    print(n1 / n2)
    print(n1 // n2)
    print(n1 % n2)
    print(sqrt(n1 ** 10 + n2 ** 10))
    return n1+n2






if __name__ == '__main__':
    n1 = int(input())
    n2 = int(input())
    main(n1, n2)

